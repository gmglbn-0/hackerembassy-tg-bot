function LetIn(username, date) {
  // check that space is open
  let state = StatusRepository.getSpaceLastState();
  if (!state?.open) {
    return false;
  }

  let userstate = {
    inside: true,
    date: date,
    username: username,
  };

  StatusRepository.pushPeopleState(userstate);

  return true;
}

function LetOut(username, date) {
  let state = StatusRepository.getSpaceLastState();
  if (!state?.open) {
    return false;
  }

  let userstate = {
    inside: false,
    date: date,
    username: username,
  };

  StatusRepository.pushPeopleState(userstate);

  return true;
}

